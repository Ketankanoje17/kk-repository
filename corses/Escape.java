package corses;
//t
public class Escape {
    public static void main(String[] args) {
        System.out.println("ketan 'abc'kanoje");
        System.out.println("ketan \"abc\"kanoje");  //double quote in same line
        System.out.println("ketan \\abc\\kanoje");  //double backslash solve
        System.out.println("ketan \n kanoje"); //using backslash \n direcly go second line after \n
        System.out.println("ketan\tkanoje"); //using backslash \t direcly provide space tab \n
    }
}
